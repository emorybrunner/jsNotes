//Assignment Operator
let r = 5; //assigning the value of 5 to the variable 'r'


//Addition Assignment
r += 1; //same as r = r + 1


//Subtraction Assignment
r -= 1 //same as r = r - 1


//Multiplication Assignment
r *= 1 //same as r = r * 1


//Division Assignment
r /= 1 //same as r = r / 1


//Modulus/Remainder Assignment
r %= 1 //same as r = r % 1


//Exponential Assignment
r **= 1 //same as r = r ^ 1 or r = r ** 1

