//* PARAMETERS

function hi(name) {
    console.log(`Hi ${name}!`);
}
hi('Keagan');
//'Keagan' is an argument used to change the output
hi('Justin');
hi('Ro');

function addIt(x, y) {
    console.log(x + y);
}
addIt(2, 9);
addIt(8, 15);
addIt(234678, 2917350);

